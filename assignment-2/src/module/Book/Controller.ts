export const getStringToIntFrom = async (req: Request, res: Response) => {

    try {

        // from string to int find & also by year conditionally...

        const stringToInt = await BookModel.aggregate([
            {
                $match: {
                    publicationYear: { $gt: 2020 },
                    price: { $type: "string" }
                }
            },
            {
                $addFields: {
                    price: { $toInt: "$price" }
                }
            }
        ])

        console.log(stringToInt);

        res.status(200).json({ data: stringToInt });

    } catch (error) {
        console.log(error);
    }
}


